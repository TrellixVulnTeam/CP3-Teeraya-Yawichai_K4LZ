x = int(input("Please enter a number of X: "))
y = int(input("Please enter a number of Y: "))
addition = x + y
subtraction = x - y
multiplication = x * y
division = x / y

print(x, "+", y, "=", int(addition))
print(x, "-", y, "=", int(subtraction))
print(x, "*", y, "=", int(multiplication))
print(x, "/", y, "=", int(division))
